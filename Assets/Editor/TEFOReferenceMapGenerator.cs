    private static bool IsNearRoad(MapContext context, int x, int y)
    {
        for (int dx = -2; dx <= 2; dx++)
        {
            for (int dy = -2; dy <= 2; dy++)
            {
                int px = x + dx;
                int py = y + dy;
                if (IsInsideMap(px, py) && context.Road[px, py])
                {
                    return true;
                }
            }
        }

        return false;
    }

    private static void PlaceFenceLine(MapContext context, Transform parent, int x0, int y0, int x1, int y1)
    {
        int dx = Math.Sign(x1 - x0);
        int dy = Math.Sign(y1 - y0);
        int x = x0;
        int y = y0;
        while (true)
        {
            PlacePrefabIfAvailable(context, PickPrefab(context, PrefabKind.Prop, "fence", "fence", dx != 0 ? "h" : "v"), parent, x, y, false, false, false, 0);
            if (x == x1 && y == y1)
            {
                break;
            }

            if (x != x1)
            {
                x += dx;
            }

            if (y != y1)
            {
                y += dy;
            }
        }
    }

    private static PrefabChoice PickPrefab(MapContext context, PrefabKind kind, string label, params string[] tokens)
    {
        List<PrefabChoice> choices = context.Library.FindPrefabs(kind, tokens);
        if (choices.Count == 0)
        {
            context.Report.RecordMissingPrefab(kind, label);
            return PrefabChoice.Invalid;
        }

        if (choices.Count == 1)
        {
            context.Report.RecordSinglePrefabVariant(kind, label);
        }

        return choices[context.Random.Next(choices.Count)];
    }

    private static bool PlacePrefabIfAvailable(MapContext context, PrefabChoice choice, Transform parent, int x, int y, bool isBuilding, bool allowWater, bool allowMainPath, int margin)
    {
        if (!choice.IsValid || parent == null)
        {
            return false;
        }

        Vector2Int footprint = GetPrefabFootprint(choice.Prefab, isBuilding);
        if (!IsAreaClear(context, x, y, footprint.x, footprint.y, margin, allowWater, allowMainPath))
        {
            context.Report.SkippedPlacements.Add($"{choice.Path} at {x},{y}: blocked by road, water, path, spawn, or another generated object.");
            return false;
        }

        GameObject instance = PrefabUtility.InstantiatePrefab(choice.Prefab) as GameObject;
        if (instance == null)
        {
            context.Report.Warnings.Add($"Could not instantiate prefab: {choice.Path}");
            return false;
        }

        Undo.RegisterCreatedObjectUndo(instance, $"Place {choice.Prefab.name}");
        Undo.SetTransformParent(instance.transform, parent, $"Parent {choice.Prefab.name}");
        instance.transform.position = new Vector3(x + 0.5f, y + 0.5f, instance.transform.position.z);
        instance.transform.rotation = Quaternion.identity;
        instance.transform.localScale = Vector3.one;

        MarkOccupied(context, x, y, footprint.x, footprint.y, margin);
        if (isBuilding)
        {
            context.Report.BuildingsPlaced++;
            context.Report.Increment(context.Report.BuildingsPlacedByCategory, choice.Category);
            if (!choice.HasCollider)
            {
                PaintBuildingCollision(context, x, y, footprint.x, footprint.y);
            }
        }
        else
        {
            context.Report.PropsPlaced++;
            context.Report.Increment(context.Report.PropsPlacedByCategory, choice.Category);
        }

        return true;
    }

    private static Vector2Int GetPrefabFootprint(GameObject prefab, bool isBuilding)
    {
        SpriteRenderer renderer = prefab.GetComponent<SpriteRenderer>();
        if (renderer == null || renderer.sprite == null)
        {
            return isBuilding ? new Vector2Int(3, 3) : new Vector2Int(1, 1);
        }

        Vector3 size = renderer.sprite.bounds.size;
        int width = Mathf.Clamp(Mathf.CeilToInt(size.x), isBuilding ? 3 : 1, isBuilding ? 8 : 3);
        int height = Mathf.Clamp(Mathf.CeilToInt(size.y), isBuilding ? 3 : 1, isBuilding ? 8 : 3);
        return new Vector2Int(width, height);
    }

    private static bool IsAreaClear(MapContext context, int centerX, int centerY, int width, int height, int margin, bool allowWater, bool allowMainPath)
    {
        int left = centerX - width / 2 - margin;
        int right = centerX + (width - 1) / 2 + margin;
        int bottom = centerY - height / 2 - margin;
        int top = centerY + (height - 1) / 2 + margin;

        for (int x = left; x <= right; x++)
        {
            for (int y = bottom; y <= top; y++)
            {
                if (!IsInsideMap(x, y) || IsNearSpawn(x, y))
                {
                    return false;
                }

                if (context.Occupied[x, y] || context.Road[x, y] || (!allowWater && context.Water[x, y]) || (!allowMainPath && context.MainPath[x, y]))
                {
                    return false;
                }
            }
        }

        return true;
    }

    private static void ReserveArea(MapContext context, int centerX, int centerY, int width, int height, int margin, bool allowWater, bool allowMainPath, bool allowRoad)
    {
        int left = centerX - width / 2 - margin;
        int right = centerX + (width - 1) / 2 + margin;
        int bottom = centerY - height / 2 - margin;
        int top = centerY + (height - 1) / 2 + margin;

        for (int x = left; x <= right; x++)
        {
            for (int y = bottom; y <= top; y++)
            {
                if (!IsInsideMap(x, y))
                {
                    continue;
                }

                context.Occupied[x, y] = true;
                if (!allowWater)
                {
                    context.Water[x, y] = false;
                }

                if (!allowMainPath)
                {
                    context.MainPath[x, y] = false;
                }

                if (!allowRoad)
                {
                    context.Road[x, y] = false;
                }
            }
        }
    }

    private static void MarkOccupied(MapContext context, int centerX, int centerY, int width, int height, int margin)
    {
        int left = centerX - width / 2 - margin;
        int right = centerX + (width - 1) / 2 + margin;
        int bottom = centerY - height / 2 - margin;
        int top = centerY + (height - 1) / 2 + margin;

        for (int x = left; x <= right; x++)
        {
            for (int y = bottom; y <= top; y++)
            {
                if (IsInsideMap(x, y))
                {
                    context.Occupied[x, y] = true;
                }
            }
        }
    }

