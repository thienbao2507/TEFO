    [MenuItem("TEFO/Map/Demo/Generate Polished Reference Map Demo")]
    public static void GeneratePolishedReferenceMapDemo()
    {
        DemoReport report = new DemoReport();
        BoundsInt demoBounds = new BoundsInt(0, 0, 0, MapWidth, MapHeight, 1);

        GameObject root = TEFOMapLayerSetupTool.SetupLayerStack(false);
        Transform gridTransform = FindGridTransform(root);
        Dictionary<string, Tilemap> tilemaps = FindTilemaps(gridTransform, report);

        bool isRegeneratingDemo = root.transform.Find(DemoMarkerName) != null;
        if (!isRegeneratingDemo && HasExistingTiles(tilemaps, demoBounds))
        {
            report.SkippedAreas.Add("Generation cancelled because TEFO_Map already has tiles and no demo metadata marker was found. This protects manually painted map layers.");
            WriteReport(report);
            EditorUtility.DisplayDialog(
                "TEFO demo map not generated",
                "Existing map tiles were found, but they were not marked as generated by TEFOReferenceMapGenerator. Clear them manually or keep a generated marker if you want this tool to repaint them.",
                "OK");
            return;
        }

        ClearGeneratedDemo(root, tilemaps, demoBounds);

        AssetLibrary library = AssetLibrary.Load(report);
        TileSet tiles = TileSet.Resolve(library, report);
        MapContext context = new MapContext(tilemaps, library, tiles, report, new System.Random(Seed));

        ReserveSpawn(context);
        PaintBaseTerrain(context);
        PaintForestZone(context);
        PaintRuralVillage(context);
        PaintNatureTransition(context);
        PaintUrbanTown(context);
        PaintBeachAndOcean(context);
        PaintDecals(context);

        Transform propsParent = CreateGeneratedContainer(root.transform, DemoPropsName);
        Transform buildingsParent = CreateGeneratedContainer(root.transform, DemoBuildingsName);
        PlaceForestProps(context, propsParent);
        PlaceVillagePropsAndBuildings(context, propsParent, buildingsParent);
        PlaceTransitionPropsAndLandmarks(context, propsParent, buildingsParent);
        PlaceTownPropsAndBuildings(context, propsParent, buildingsParent);
        PlaceBeachPropsAndBuildings(context, propsParent, buildingsParent);

        MovePlayerAndCamera(report);
        CreateDemoMarker(root.transform);

        foreach (Tilemap tilemap in tilemaps.Values)
        {
            tilemap.CompressBounds();
            EditorUtility.SetDirty(tilemap);
        }

        EditorSceneManager.MarkSceneDirty(root.scene);
        Selection.activeGameObject = root;
        WriteReport(report);
        AssetDatabase.SaveAssets();
        AssetDatabase.Refresh();

        Debug.Log($"TEFO polished reference demo map generated. Report: {ReportPath}");
    }

