    private static void PaintRoadDetails(MapContext context, int minX, int maxX, int minY, int maxY, int attempts)
    {
        for (int i = 0; i < attempts; i++)
        {
            int x = context.Random.Next(minX, maxX + 1);
            int y = context.Random.Next(minY, maxY + 1);
            if (!IsInsideMap(x, y) || !context.Road[x, y])
            {
                continue;
            }

            TileBase detail = PickRandomTile(context, "Road", "crack", "detail", "drain");
            if (detail != null)
            {
                PaintCell(context, "Decals_Back", detail, x, y);
            }
        }
    }

    private static void PaintNoiseVariation(MapContext context, int minX, int maxX, int minY, int maxY, string category, string[] keywords, int attempts, int radius)
    {
        for (int i = 0; i < attempts; i++)
        {
            int x = context.Random.Next(minX, maxX + 1);
            int y = context.Random.Next(minY, maxY + 1);
            PaintIrregularBlob(context, category == "Water" ? "Water" : "Grass", category, keywords, x, y, radius + context.Random.Next(0, 2), radius, category == "Water", false);
        }
    }

    private static void PaintIrregularBlob(MapContext context, string layerName, string tileCategory, string[] keywords, int centerX, int centerY, int radiusX, int radiusY, bool markWater, bool markPath)
    {
        for (int x = centerX - radiusX - 2; x <= centerX + radiusX + 2; x++)
        {
            for (int y = centerY - radiusY - 2; y <= centerY + radiusY + 2; y++)
            {
                if (!IsInsideMap(x, y))
                {
                    continue;
                }

                float nx = (x - centerX) / Mathf.Max(1f, radiusX);
                float ny = (y - centerY) / Mathf.Max(1f, radiusY);
                float distance = Mathf.Sqrt(nx * nx + ny * ny);
                float edge = 1f + (Noise01(x, y, centerX + centerY) - 0.5f) * 0.45f;
                if (distance > edge)
                {
                    continue;
                }

                PaintCell(context, layerName, PickRandomTile(context, tileCategory, keywords), x, y);
                if (markWater)
                {
                    context.Water[x, y] = true;
                }

                if (markPath)
                {
                    context.MainPath[x, y] = true;
                }
            }
        }
    }

    private static void PaintWindingPath(MapContext context, int radius, params Vector2Int[] points)
    {
        int globalStep = 0;
        for (int i = 0; i < points.Length - 1; i++)
        {
            Vector2 start = points[i];
            Vector2 end = points[i + 1];
            Vector2 direction = end - start;
            Vector2 normal = direction.sqrMagnitude > 0.01f ? new Vector2(-direction.y, direction.x).normalized : Vector2.up;
            int steps = Mathf.Max(1, Mathf.RoundToInt(direction.magnitude * 3f));

            for (int step = 0; step <= steps; step++)
            {
                float t = step / (float)steps;
                float wobble = Mathf.Sin((globalStep + Seed) * 0.31f) * 1.15f + Mathf.Sin((globalStep + Seed) * 0.11f) * 0.7f;
                Vector2 point = Vector2.Lerp(start, end, t) + normal * wobble;
                int x = Mathf.RoundToInt(point.x);
                int y = Mathf.RoundToInt(point.y);
                PaintBrush(context, "Dirt", x, y, radius + (Noise01(x, y, 23) > 0.72f ? 1 : 0), "Dirt", new[] { "center", "path" }, false, true);
                globalStep++;
            }
        }
    }

    private static void PaintRoadSegment(MapContext context, int x0, int y0, int x1, int y1, int width, bool withSidewalk)
    {
        bool vertical = x0 == x1;
        int half = width / 2;
        int minX = Mathf.Min(x0, x1);
        int maxX = Mathf.Max(x0, x1);
        int minY = Mathf.Min(y0, y1);
        int maxY = Mathf.Max(y0, y1);

        if (vertical)
        {
            for (int y = minY; y <= maxY; y++)
            {
                for (int dx = -half; dx <= half; dx++)
                {
                    int x = x0 + dx;
                    PaintRoadCell(context, x, y, "v");
                }

                if (withSidewalk)
                {
                    PaintCell(context, "Curb", PickRandomTile(context, "Curb", "left"), x0 - half - 1, y);
                    PaintCell(context, "Curb", PickRandomTile(context, "Curb", "right"), x0 + half + 1, y);
                    PaintCell(context, "Sidewalk", PickRandomTile(context, "Sidewalk", "center"), x0 - half - 2, y);
                    PaintCell(context, "Sidewalk", PickRandomTile(context, "Sidewalk", "center"), x0 + half + 2, y);
                }

                if (y % 5 == 0)
                {
                    PaintCell(context, "Road_Marking", PickRandomTile(context, "Road_Marking", "line", "v"), x0, y);
                }
            }
        }
        else
        {
            for (int x = minX; x <= maxX; x++)
            {
                for (int dy = -half; dy <= half; dy++)
                {
                    int y = y0 + dy;
                    PaintRoadCell(context, x, y, "h");
                }

                if (withSidewalk)
                {
                    PaintCell(context, "Curb", PickRandomTile(context, "Curb", "bottom"), x, y0 - half - 1);
                    PaintCell(context, "Curb", PickRandomTile(context, "Curb", "top"), x, y0 + half + 1);
                    PaintCell(context, "Sidewalk", PickRandomTile(context, "Sidewalk", "center"), x, y0 - half - 2);
                    PaintCell(context, "Sidewalk", PickRandomTile(context, "Sidewalk", "center"), x, y0 + half + 2);
                }

                if (x % 5 == 0)
                {
                    PaintCell(context, "Road_Marking", PickRandomTile(context, "Road_Marking", "line", "h"), x, y0);
                }
            }
        }
    }

    private static void PaintRoadCell(MapContext context, int x, int y, string orientation)
    {
        if (!IsInsideMap(x, y))
        {
            return;
        }

        TileBase road = PickWeightedTile(context, "Road", new[] { "straight", orientation }, new[] { "center", "detail" }, 25);
        PaintCell(context, "Road", road, x, y);
        context.Road[x, y] = true;
        context.MainPath[x, y] = true;
    }

    private static void PaintCrosswalk(MapContext context, int centerX, int centerY)
    {
        for (int dx = -4; dx <= 4; dx++)
        {
            PaintCell(context, "Road_Marking", PickRandomTile(context, "Road_Marking", "crosswalk", "h"), centerX + dx, centerY - 4);
            PaintCell(context, "Road_Marking", PickRandomTile(context, "Road_Marking", "crosswalk", "h"), centerX + dx, centerY + 4);
        }

        for (int dy = -4; dy <= 4; dy++)
        {
            PaintCell(context, "Road_Marking", PickRandomTile(context, "Road_Marking", "crosswalk", "v"), centerX - 4, centerY + dy);
            PaintCell(context, "Road_Marking", PickRandomTile(context, "Road_Marking", "crosswalk", "v"), centerX + 4, centerY + dy);
        }
    }

    private static void PaintLineCells(MapContext context, string layerName, TileBase tile, int x0, int y0, int x1, int y1)
    {
        int dx = Math.Sign(x1 - x0);
        int dy = Math.Sign(y1 - y0);
        int x = x0;
        int y = y0;
        PaintCell(context, layerName, tile, x, y);
        while (x != x1 || y != y1)
        {
            if (x != x1)
            {
                x += dx;
            }

            if (y != y1)
            {
                y += dy;
            }

            PaintCell(context, layerName, tile, x, y);
        }
    }

    private static void PaintBrush(MapContext context, string layerName, int centerX, int centerY, int radius, string tileCategory, string[] keywords, bool markWater, bool markPath)
    {
        int r2 = radius * radius;
        for (int x = centerX - radius; x <= centerX + radius; x++)
        {
            for (int y = centerY - radius; y <= centerY + radius; y++)
            {
                int dx = x - centerX;
                int dy = y - centerY;
                if (dx * dx + dy * dy > r2 || !IsInsideMap(x, y))
                {
                    continue;
                }

                PaintCell(context, layerName, PickRandomTile(context, tileCategory, keywords), x, y);
                if (markWater)
                {
                    context.Water[x, y] = true;
                }

                if (markPath)
                {
                    context.MainPath[x, y] = true;
                }
            }
        }
    }

    private static void PaintCell(MapContext context, string layerName, TileBase tile, int x, int y)
    {
        if (tile == null || !IsInsideMap(x, y))
        {
            return;
        }

        if (!context.Tilemaps.TryGetValue(layerName, out Tilemap tilemap) || tilemap == null)
        {
            context.Report.RecordMissingLayer(layerName);
            return;
        }

        Vector3Int position = new Vector3Int(x, y, 0);
        tilemap.SetTile(position, tile);
        context.Report.RecordTilePainted(layerName, position);
    }

    private static TileBase PickRandomTile(MapContext context, string category, params string[] keywords)
    {
        List<TileBase> candidates = context.Library.FindTiles(category, keywords);
        if (candidates.Count == 0)
        {
            TileBase fallback = context.Tiles.GetFallbackTile(category);
            if (fallback == null)
            {
                context.Report.RecordMissingTile(category, string.Join(" ", keywords));
            }

            return fallback;
        }

        if (candidates.Count == 1)
        {
            context.Report.RecordSingleVariant(category, keywords);
        }

        return candidates[context.Random.Next(candidates.Count)];
    }

    private static TileBase PickWeightedTile(MapContext context, string category, string[] primaryKeywords, string[] variationKeywords, int variationPercent)
    {
        if (context.Random.Next(100) < variationPercent)
        {
            TileBase variation = PickRandomTile(context, category, variationKeywords);
            if (variation != null)
            {
                return variation;
            }
        }

        TileBase primary = PickRandomTile(context, category, primaryKeywords);
        return primary ?? PickRandomTile(context, category);
    }

    private static TileBase PickRandomFromList(MapContext context, List<TileBase> tiles, TileBase fallback)
    {
        if (tiles.Count == 0)
        {
            return fallback;
        }

        return tiles[context.Random.Next(tiles.Count)];
    }

    private static void PlaceForestProps(MapContext context, Transform propsParent)
    {
        List<Vector2Int> clusters = new List<Vector2Int>
        {
            new Vector2Int(7, 72),
            new Vector2Int(10, 48),
            new Vector2Int(13, 20),
            new Vector2Int(27, 75),
            new Vector2Int(34, 30)
        };

        foreach (Vector2Int cluster in clusters)
        {
            int count = cluster.x < 15 ? 42 : 26;
            for (int i = 0; i < count; i++)
            {
                int x = Mathf.Clamp(Mathf.RoundToInt(cluster.x + NextGaussian(context.Random) * 6f), 1, 43);
                int y = Mathf.Clamp(Mathf.RoundToInt(cluster.y + NextGaussian(context.Random) * 8f), 2, 87);
                PrefabChoice tree = PickPrefab(context, PrefabKind.Prop, "forest tree", "tree", i % 3 == 0 ? "pine" : "oak");
                PlacePrefabIfAvailable(context, tree, propsParent, x, y, false, false, false, 0);
            }
        }

        for (int i = 0; i < 80; i++)
        {
            int x = context.Random.Next(2, 50);
            int y = context.Random.Next(3, 87);
            PrefabChoice prop = i % 4 == 0
                ? PickPrefab(context, PrefabKind.Prop, "forest rock", "rock")
