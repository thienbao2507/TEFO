    private static void PaintBuildingCollision(MapContext context, int x, int y, int width, int height)
    {
        if (context.Tiles.Collision == null)
        {
            return;
        }

        int left = x - width / 2;
        int bottom = y - height / 2;
        for (int px = left; px < left + width; px++)
        {
            for (int py = bottom; py < bottom + height; py++)
            {
                if (IsInsideMap(px, py) && !context.Road[px, py] && !context.MainPath[px, py])
                {
                    PaintCell(context, "Collision", context.Tiles.Collision, px, py);
                }
            }
        }
    }

