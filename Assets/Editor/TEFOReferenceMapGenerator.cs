    private sealed class AssetLibrary
    {
        private readonly List<TileAsset> tiles;
        private readonly List<PrefabAsset> prefabs;
        private readonly Dictionary<string, TileBase> tileQueryCache = new Dictionary<string, TileBase>();

        private AssetLibrary(List<TileAsset> tiles, List<PrefabAsset> prefabs)
        {
            this.tiles = tiles;
            this.prefabs = prefabs;
        }

        public static AssetLibrary Load(DemoReport report)
        {
            List<TileAsset> loadedTiles = new List<TileAsset>();
            HashSet<string> loadedTilePaths = new HashSet<string>(StringComparer.OrdinalIgnoreCase);
            foreach (string root in TileRoots)
            {
                if (!Directory.Exists(root))
                {
                    continue;
                }

                foreach (string rawPath in Directory.GetFiles(root, "*.asset", SearchOption.AllDirectories))
                {
                    string path = NormalizePath(rawPath);
                    if (!loadedTilePaths.Add(path))
                    {
                        continue;
                    }

                    TileBase tile = AssetDatabase.LoadAssetAtPath<TileBase>(path);
                    if (tile != null)
                    {
                        loadedTiles.Add(new TileAsset(path, tile));
                    }
                }
            }

            List<PrefabAsset> loadedPrefabs = new List<PrefabAsset>();
            HashSet<string> loadedPrefabPaths = new HashSet<string>(StringComparer.OrdinalIgnoreCase);
            foreach (string root in PrefabRoots)
            {
                if (!Directory.Exists(root))
                {
                    continue;
                }

                foreach (string rawPath in Directory.GetFiles(root, "*.prefab", SearchOption.AllDirectories))
                {
                    string path = NormalizePath(rawPath);
                    if (!loadedPrefabPaths.Add(path))
                    {
                        continue;
                    }

                    GameObject prefab = AssetDatabase.LoadAssetAtPath<GameObject>(path);
                    if (prefab != null)
                    {
                        loadedPrefabs.Add(new PrefabAsset(path, prefab));
                    }
                }
            }

            report.AvailableTileCategories.AddRange(loadedTiles.Select(tile => tile.Category).Distinct().OrderBy(category => category));
            report.AvailablePrefabCategories.AddRange(loadedPrefabs.Select(prefab => $"{prefab.Kind}: {prefab.Category}").Distinct().OrderBy(category => category));
            RecordMissingImportantCategories(report, loadedTiles, loadedPrefabs);

            return new AssetLibrary(loadedTiles, loadedPrefabs);
        }

        public TileBase FindTile(DemoReport report, string category, string label, string[] requiredTokens, string[] fallbackCategories)
        {
            string cacheKey = $"{category}|{label}|{string.Join(",", requiredTokens)}|{string.Join(",", fallbackCategories)}";
            if (tileQueryCache.TryGetValue(cacheKey, out TileBase cached))
            {
                return cached;
            }

            List<TileAsset> candidates = FindTileAssets(category, requiredTokens);
            if (candidates.Count > 0)
            {
                TileAsset selected = candidates[0];
                tileQueryCache[cacheKey] = selected.Tile;
                return selected.Tile;
            }

            List<TileAsset> categoryFallbacks = FindTileAssets(category, new string[0]);
            if (categoryFallbacks.Count > 0)
            {
                TileAsset fallback = categoryFallbacks[0];
                report.FallbackTilesUsed.Add($"{label}: used {fallback.Path} from {category} because exact tokens were missing.");
                tileQueryCache[cacheKey] = fallback.Tile;
                return fallback.Tile;
            }

            foreach (string fallbackCategory in fallbackCategories)
            {
                List<TileAsset> fallbackCandidates = FindTileAssets(fallbackCategory, requiredTokens);
                if (fallbackCandidates.Count == 0)
                {
                    fallbackCandidates = FindTileAssets(fallbackCategory, new string[0]);
                }

                if (fallbackCandidates.Count > 0)
                {
                    TileAsset fallback = fallbackCandidates[0];
                    report.FallbackTilesUsed.Add($"{label}: used {fallback.Path} from fallback category {fallbackCategory}.");
                    tileQueryCache[cacheKey] = fallback.Tile;
                    return fallback.Tile;
                }
            }

            report.RecordMissingTile(category, label);
            tileQueryCache[cacheKey] = null;
            return null;
        }

        public List<TileBase> FindTiles(string category, params string[] tokens)
        {
            return FindTileAssets(category, tokens).Select(asset => asset.Tile).Distinct().ToList();
        }

        private List<TileAsset> FindTileAssets(string category, IEnumerable<string> tokens)
        {
            string[] normalizedTokens = tokens == null ? new string[0] : tokens.Where(token => !string.IsNullOrWhiteSpace(token)).Select(Normalize).ToArray();
            List<TileAsset> categoryAssets = tiles
                .Where(tile => string.Equals(tile.Category, category, StringComparison.OrdinalIgnoreCase))
                .ToList();

            if (normalizedTokens.Length == 0)
            {
                return categoryAssets.OrderBy(tile => tile.Path).ToList();
            }

            List<TileAsset> exact = categoryAssets
                .Where(tile => normalizedTokens.All(tile.SearchText.Contains))
                .OrderByDescending(tile => tile.Score(normalizedTokens))
                .ThenBy(tile => tile.Path)
                .ToList();

            if (exact.Count > 0)
            {
                return exact;
            }

            return categoryAssets
                .Where(tile => normalizedTokens.Any(tile.SearchText.Contains))
                .OrderByDescending(tile => tile.Score(normalizedTokens))
                .ThenBy(tile => tile.Path)
                .ToList();
        }

        public List<PrefabChoice> FindPrefabs(PrefabKind kind, params string[] tokens)
        {
            string[] normalizedTokens = tokens == null ? new string[0] : tokens.Where(token => !string.IsNullOrWhiteSpace(token)).Select(Normalize).ToArray();
            IEnumerable<PrefabAsset> candidates = prefabs.Where(prefab => prefab.Kind == kind);
            if (normalizedTokens.Length > 0)
            {
                candidates = candidates.Where(prefab => normalizedTokens.Any(prefab.SearchText.Contains));
            }

            return candidates
                .OrderByDescending(prefab => prefab.Score(normalizedTokens))
                .ThenBy(prefab => prefab.Path)
                .Select(prefab => prefab.ToChoice())
                .ToList();
        }

        private static void RecordMissingImportantCategories(DemoReport report, List<TileAsset> loadedTiles, List<PrefabAsset> loadedPrefabs)
        {
            string[] importantTiles =
            {
                "Grass", "Forest", "Dirt", "VillagePlaza", "Sand", "Water", "Road", "Road_Marking", "Sidewalk", "Curb", "Collision"
            };

            foreach (string category in importantTiles)
            {
                if (!loadedTiles.Any(tile => string.Equals(tile.Category, category, StringComparison.OrdinalIgnoreCase)))
                {
                    report.MissingImportantCategories.Add($"Tile: {category}");
                }
            }

            if (!loadedPrefabs.Any(prefab => prefab.Kind == PrefabKind.Prop && prefab.SearchText.Contains("tree")))
            {
                report.MissingImportantCategories.Add("Prop: tree");
            }

            if (!loadedPrefabs.Any(prefab => prefab.Kind == PrefabKind.Prop && prefab.SearchText.Contains("palm")))
            {
                report.MissingImportantCategories.Add("Prop: palm");
            }

            if (!loadedPrefabs.Any(prefab => prefab.Kind == PrefabKind.Building && prefab.SearchText.Contains("garage")))
            {
                report.MissingImportantCategories.Add("Building: garage");
            }
        }

        private static string NormalizePath(string path)
        {
            return path.Replace('\\', '/');
        }
    }

    private sealed class TileAsset
    {
        public readonly string Path;
        public readonly string SearchText;
        public readonly string Category;
        public readonly TileBase Tile;

        public TileAsset(string path, TileBase tile)
        {
            Path = path;
            Tile = tile;
            SearchText = Normalize($"{path} {System.IO.Path.GetFileNameWithoutExtension(path)}");
            Category = DetermineTileCategory(path);
        }

        public int Score(IEnumerable<string> normalizedTokens)
        {
            int score = 0;
            foreach (string token in normalizedTokens)
            {
                if (SearchText.Contains(token))
                {
                    score += 10;
                }
            }

            return score;
        }

        private static string DetermineTileCategory(string path)
        {
            string text = Normalize(path);
            if (text.Contains("road_marking") || text.Contains("roadmarking") || text.Contains("crosswalk") || text.Contains("parking_line") || text.Contains("road_line") || text.Contains("dash"))
            {
                return "Road_Marking";
            }

            if (text.Contains("villageplaza") || text.Contains("village_plaza") || text.Contains("plaza"))
            {
                return "VillagePlaza";
            }

            if (text.Contains("forest"))
            {
                return "Forest";
            }

            if (text.Contains("dirt") || text.Contains("path"))
            {
                return "Dirt";
            }

            if (text.Contains("sidewalk"))
            {
                return "Sidewalk";
            }

            if (text.Contains("curb") || text.Contains("kerb"))
            {
                return "Curb";
            }

            if (text.Contains("sand") || text.Contains("beach"))
            {
                return "Sand";
            }

            if (text.Contains("water") || text.Contains("ocean") || text.Contains("pond") || text.Contains("shore") || text.Contains("foam"))
            {
                return "Water";
            }

            if (text.Contains("decal") || text.Contains("oil") || text.Contains("stain") || text.Contains("moss") || text.Contains("leaf"))
            {
                return "Decals";
            }

            if (text.Contains("collision") || text.Contains("blocker"))
            {
                return "Collision";
            }

            if (text.Contains("road"))
            {
                return "Road";
            }

            return "Grass";
        }
    }

    private sealed class PrefabAsset
    {
        public readonly string Path;
        public readonly string SearchText;
        public readonly string Category;
        public readonly GameObject Prefab;
        public readonly PrefabKind Kind;
        public readonly bool HasCollider;

        public PrefabAsset(string path, GameObject prefab)
        {
            Path = path;
            Prefab = prefab;
            SearchText = Normalize($"{path} {System.IO.Path.GetFileNameWithoutExtension(path)}");
            string normalizedPath = path.Replace('\\', '/');
            Kind = normalizedPath.Contains("/Buildings/") ? PrefabKind.Building : PrefabKind.Prop;
            Category = DeterminePrefabCategory(normalizedPath, Kind);
            HasCollider = prefab.GetComponent<Collider2D>() != null;
        }

        public PrefabChoice ToChoice()
        {
            return new PrefabChoice(Prefab, Kind, Category, Path, HasCollider);
        }

        public int Score(IEnumerable<string> normalizedTokens)
        {
            int score = 0;
            foreach (string token in normalizedTokens)
            {
                if (SearchText.Contains(token))
                {
                    score += 10;
                }
            }

            return score;
        }

        private static string DeterminePrefabCategory(string path, PrefabKind kind)
        {
            string marker = kind == PrefabKind.Building ? "/Buildings/" : "/Props/";
            int markerIndex = path.IndexOf(marker, StringComparison.OrdinalIgnoreCase);
            if (markerIndex >= 0)
            {
                string remainder = path.Substring(markerIndex + marker.Length);
                int slash = remainder.IndexOf('/');
                if (slash > 0)
                {
                    return remainder.Substring(0, slash);
                }
            }

            return kind == PrefabKind.Building ? "Building" : "Prop";
        }
    }

    private readonly struct PrefabChoice
    {
        public static readonly PrefabChoice Invalid = new PrefabChoice(null, PrefabKind.Prop, string.Empty, string.Empty, false);

        public readonly GameObject Prefab;
        public readonly PrefabKind Kind;
        public readonly string Category;
        public readonly string Path;
        public readonly bool HasCollider;
        public bool IsValid => Prefab != null;

        public PrefabChoice(GameObject prefab, PrefabKind kind, string category, string path, bool hasCollider)
        {
            Prefab = prefab;
            Kind = kind;
            Category = category;
            Path = path;
            HasCollider = hasCollider;
        }
    }

    private sealed class DemoReport
    {
        public int PropsPlaced;
        public int BuildingsPlaced;
        public readonly Dictionary<string, HashSet<Vector3Int>> TilesPaintedByLayer = new Dictionary<string, HashSet<Vector3Int>>(StringComparer.OrdinalIgnoreCase);
        public readonly Dictionary<string, int> PropsPlacedByCategory = new Dictionary<string, int>(StringComparer.OrdinalIgnoreCase);
        public readonly Dictionary<string, int> BuildingsPlacedByCategory = new Dictionary<string, int>(StringComparer.OrdinalIgnoreCase);
        public readonly List<string> AvailableTileCategories = new List<string>();
        public readonly List<string> AvailablePrefabCategories = new List<string>();
        public readonly List<string> MissingImportantCategories = new List<string>();
        public readonly List<string> MissingTileCategories = new List<string>();
        public readonly List<string> MissingPrefabCategories = new List<string>();
        public readonly List<string> FallbackTilesUsed = new List<string>();
        public readonly List<string> SkippedPlacements = new List<string>();
        public readonly List<string> SkippedAreas = new List<string>();
        public readonly List<string> SceneObjectsMoved = new List<string>();
        public readonly List<string> Warnings = new List<string>();
        private readonly HashSet<string> missingLayers = new HashSet<string>(StringComparer.OrdinalIgnoreCase);
        private readonly HashSet<string> singleVariantWarnings = new HashSet<string>(StringComparer.OrdinalIgnoreCase);
        private readonly HashSet<string> missingTileWarnings = new HashSet<string>(StringComparer.OrdinalIgnoreCase);
        private readonly HashSet<string> missingPrefabWarnings = new HashSet<string>(StringComparer.OrdinalIgnoreCase);

        public void RecordTilePainted(string layerName, Vector3Int position)
        {
            if (!TilesPaintedByLayer.TryGetValue(layerName, out HashSet<Vector3Int> positions))
            {
                positions = new HashSet<Vector3Int>();
                TilesPaintedByLayer[layerName] = positions;
            }

            positions.Add(position);
        }

        public void RecordMissingLayer(string layerName)
        {
            if (missingLayers.Add(layerName))
            {
                Warnings.Add($"Tilemap layer missing or unavailable: {layerName}");
            }
        }

        public void RecordSingleVariant(string category, IEnumerable<string> keywords)
        {
            string key = $"{category} {string.Join(" ", keywords ?? new string[0])}";
            if (singleVariantWarnings.Add(key))
            {
                Warnings.Add($"Only one tile variant available for {key}; repetition may still be visible.");
            }
        }

        public void RecordSinglePrefabVariant(PrefabKind kind, string label)
        {
            string key = $"{kind}: {label}";
            if (singleVariantWarnings.Add(key))
            {
                Warnings.Add($"Only one prefab variant available for {key}; placement variation is limited.");
            }
        }

        public void RecordMissingTile(string category, string label)
        {
            string key = $"{category} ({label})";
            if (missingTileWarnings.Add(key))
            {
                MissingTileCategories.Add(key);
            }
        }

        public void RecordMissingPrefab(PrefabKind kind, string label)
        {
            string key = $"{kind}: {label}";
            if (missingPrefabWarnings.Add(key))
            {
                MissingPrefabCategories.Add(key);
            }
        }

        public void Increment(Dictionary<string, int> counts, string key)
        {
            if (string.IsNullOrWhiteSpace(key))
            {
                key = "Unknown";
            }

            counts.TryGetValue(key, out int count);
            counts[key] = count + 1;
        }
    }

    private enum PrefabKind
    {
        Prop,
        Building
    }

    private static string Normalize(string text)
    {
        return (text ?? string.Empty).Replace('\\', '/').Replace("-", "_").Replace(" ", "_").ToLowerInvariant();
    }
