    private static void PaintBaseTerrain(MapContext context)
    {
        for (int x = 0; x < MapWidth; x++)
        {
            for (int y = 0; y < MapHeight; y++)
            {
                PaintCell(context, "Ground", PickRandomTile(context, "Grass", "center"), x, y);
                PaintCell(context, "Grass", PickWeightedTile(context, "Grass", new[] { "center" }, new[] { "flowers", "dense", "rock" }, 12), x, y);
            }
        }

        PaintNoiseVariation(context, 0, 104, 0, MapHeight - 1, "Grass", new[] { "flowers", "dense", "rock" }, 80, 2);
    }

    private static void PaintForestZone(MapContext context)
    {
        for (int y = 0; y < MapHeight; y++)
        {
            int boundary = Mathf.RoundToInt(38f + Mathf.Sin(y * 0.16f) * 4f + Mathf.Sin(y * 0.43f) * 2f + (Noise01(4, y, 1) - 0.5f) * 7f);
            boundary = Mathf.Clamp(boundary, 32, 45);
            for (int x = 0; x <= boundary; x++)
            {
                bool clearing = DistanceToAny(x, y, new Vector2Int(13, 64), new Vector2Int(17, 35), new Vector2Int(31, 54)) < 4f;
                if (clearing && context.Random.NextDouble() < 0.7)
                {
                    PaintCell(context, "Grass", PickRandomTile(context, "Grass", "center"), x, y);
                    continue;
                }

                PaintCell(context, "Forest", PickWeightedTile(context, "Forest", new[] { "floor", "center" }, new[] { "edge", "corner" }, x < 9 ? 8 : 24), x, y);
            }
        }

        PaintWindingPath(context, 2, new Vector2Int(2, 69), new Vector2Int(13, 64), new Vector2Int(23, 55), new Vector2Int(30, 46));
        PaintWindingPath(context, 2, new Vector2Int(3, 34), new Vector2Int(16, 36), new Vector2Int(28, 45), new Vector2Int(42, 47));
        PaintWindingPath(context, 1, new Vector2Int(8, 18), new Vector2Int(17, 27), new Vector2Int(28, 45));

        PaintIrregularBlob(context, "Grass", "Grass", new[] { "center" }, 14, 64, 5, 4, false, false);
        PaintIrregularBlob(context, "Grass", "Grass", new[] { "flowers" }, 32, 53, 4, 3, false, false);
    }

