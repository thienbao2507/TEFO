    private static float Noise01(int x, int y, int salt)
    {
        float value = Mathf.Sin((x * 12.9898f + y * 78.233f + salt * 37.719f + Seed) * 0.0174533f) * 43758.5453f;
        return value - Mathf.Floor(value);
    }

    private static float NextGaussian(System.Random random)
    {
        double u1 = 1.0 - random.NextDouble();
        double u2 = 1.0 - random.NextDouble();
        return (float)(Math.Sqrt(-2.0 * Math.Log(u1)) * Math.Sin(2.0 * Math.PI * u2));
    }

    private static float DistanceToAny(int x, int y, params Vector2Int[] points)
    {
        float best = float.MaxValue;
        foreach (Vector2Int point in points)
        {
            float distance = Vector2Int.Distance(new Vector2Int(x, y), point);
            if (distance < best)
            {
                best = distance;
            }
        }

        return best;
    }

