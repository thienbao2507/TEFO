    private static void PaintBeachAndOcean(MapContext context)
    {
        for (int y = 0; y < MapHeight; y++)
        {
            int shoreX = Mathf.RoundToInt(113f + Mathf.Sin(y * 0.13f) * 1.7f + Mathf.Sin(y * 0.37f) * 0.9f + (Noise01(114, y, 17) - 0.5f) * 1.5f);
            shoreX = Mathf.Clamp(shoreX, 112, 115);

            for (int x = 105; x < shoreX; x++)
            {
                PaintCell(context, "Sand", PickWeightedTile(context, "Sand", new[] { "center" }, new[] { "shells", "edge" }, 18), x, y);
            }

            if (context.Tiles.HasWater)
            {
                for (int x = shoreX; x < MapWidth; x++)
                {
                    PaintCell(context, "Water", PickWeightedTile(context, "Water", new[] { "ocean", "water" }, new[] { "shore", "foam" }, x == shoreX ? 55 : 8), x, y);
                    context.Water[x, y] = true;
                }
            }

            PaintCell(context, "Decals_Back", PickRandomTile(context, "Water", "foam"), shoreX - 1, y);
            if (context.Tiles.Collision != null && y % 2 == 0)
            {
                PaintCell(context, "Collision", context.Tiles.Collision, shoreX, y);
            }
        }

        PaintIrregularBlob(context, "Sand", "Sand", new[] { "center" }, 110, 10, 5, 4, false, false);
    }

    private static void PaintDecals(MapContext context)
    {
        for (int i = 0; i < 180; i++)
        {
            int x = context.Random.Next(0, 105);
            int y = context.Random.Next(0, MapHeight);
            if (context.Road[x, y] || context.Water[x, y])
            {
                continue;
            }

            TileBase decal = PickRandomFromList(context, context.DecalVariants, context.Tiles.Decal);
            if (decal != null)
            {
                PaintCell(context, "Decals_Back", decal, x, y);
            }
        }
    }

    private static void PaintFarmPlot(MapContext context, int x, int y, int width, int height)
    {
        PaintIrregularBlob(context, "Dirt", "Dirt", new[] { "center" }, x + width / 2, y + height / 2, width / 2 + 1, height / 2 + 1, false, false);
        for (int px = x + 1; px < x + width - 1; px += 2)
        {
            for (int py = y + 1; py < y + height - 1; py++)
            {
                if (context.Random.NextDouble() < 0.75)
                {
                    PaintCell(context, "Decals_Back", PickRandomTile(context, "Grass", "flowers"), px, py);
                }
            }
        }
    }

    private static void PaintParkingLot(MapContext context, int x, int y, int width, int height)
    {
        for (int px = x; px < x + width; px++)
        {
            for (int py = y; py < y + height; py++)
            {
                PaintCell(context, "Road", PickRandomTile(context, "Road", "center"), px, py);
                context.Road[px, py] = true;
            }
        }

        for (int px = x + 1; px < x + width - 1; px += 3)
        {
            PaintLineCells(context, "Road_Marking", PickRandomTile(context, "Road_Marking", "parking", "line", "v"), px, y + 1, px, y + height - 2);
        }
    }

